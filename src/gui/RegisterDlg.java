    private void btRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRegisterActionPerformed
        try {
            if(tfPassword.getText().equals("") || tfUsername.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Bitte alle Pflichtfelder ausf체llen!");
            } else if(dba.isUsernameThere(tfUsername.getText())) {
                JOptionPane.showMessageDialog(this, "Benutzer existiert bereits!");
            } else {
                LoginPerson p = new LoginPerson(bgArt.getSelection().getActionCommand(),
                tfUsername.getText(), tfPassword.getText());
                dba.persistEntity(p);
                this.dispose();
                LoginDlg.main(null);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btRegisterActionPerformed

