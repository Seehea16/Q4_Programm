    private void btLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLoginActionPerformed
        String username = tfUsername.getText();
        try {
            if(tfPassword.getText().equals("") || tfUsername.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Bitte alle Pflichtfelder ausf√ºllen!");
            } else if(dba.isUsernameThere(username)) {
                String passwortDB = dba.findPasswordByUsername(username);
                if(passwortDB.equals(tfPassword.getText())) {
                    this.dispose();
                    String[] arr = new String[]{dba.getArt(tfUsername.getText(), tfPassword.getText()), tfUsername.getText()};
                    MainGUI.main(arr);
                    
                } else {
                    tfUsername.setText("");
                    tfPassword.setText("");
                    JOptionPane.showMessageDialog(null, "Passwort falsch!");
                }
            } else {
                tfUsername.setText("");
                tfPassword.setText("");
                JOptionPane.showMessageDialog(null, "Username falsch!");
            }
        } catch(Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btLoginActionPerformed

